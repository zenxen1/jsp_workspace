package com.fashion.product.domain;

public class EventInfo {
	private int eventinfo_id;
	private String title;
	private int rank;
	public int getEventinfo_id() {
		return eventinfo_id;
	}
	public void setEventinfo_id(int eventinfo_id) {
		this.eventinfo_id = eventinfo_id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}

	
}
